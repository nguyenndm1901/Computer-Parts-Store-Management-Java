    private void btnOKAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKAddActionPerformed
        try {
            String url = ConnectionString.getUrl();
            Connection connection = DriverManager.getConnection(url);
            String query = "INSERT INTO NhanVien (maNV,tenNV,doanhThu) VALUES(?,?,?)";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, txtMaNV.getText());
            pst.setString(2, txtTenNV.getText());
            pst.setInt(3, Integer.parseInt(txtDoanhSo.getText()));
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);
            initTable();
            JOptionPane.showMessageDialog(null, "Thêm nhân viên thành công!");
        }
        catch(Exception sqlex){
            JOptionPane.showMessageDialog(null, sqlex);
        }

        panel.setEnabled(true);
        lblMaNV.setEnabled(false);
        lblTenNV.setEnabled(false);
        lblDoanhSo.setEnabled(false);
        txtMaNV.setEnabled(false);
        txtMaNV.setText("");
        txtTenNV.setEnabled(false);
        txtTenNV.setText("");
        txtDoanhSo.setEnabled(false);
        txtDoanhSo.setText("0");
        btnOKAdd.setEnabled(false);
        btnOKEdit.setEnabled(false);
        btnOKDelete.setEnabled(false);
    }//GEN-LAST:event_btnOKAddActionPerformed

