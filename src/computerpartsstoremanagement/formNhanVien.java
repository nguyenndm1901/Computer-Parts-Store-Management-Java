    public ArrayList<Staff> staffList(){
        ArrayList<Staff> staffsList = new ArrayList<>();
        try {
            String url = ConnectionString.getUrl();
            Connection connection = DriverManager.getConnection(url);
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT * FROM NhanVien");

            while(resultSet.next()){
                Staff staff = new Staff(resultSet.getString("maNV"), resultSet.getString("tenNV"), 
                        resultSet.getInt("doanhThu"));
                staffsList.add(staff);
            }
        }
        catch(Exception sqlex){
            sqlex.printStackTrace();
        }        
        return staffsList;
    }
    
    public void initTable(){
        ArrayList<Staff> list = staffList();
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        Object[] row = new Object[3];
        for(int i = 0; i < list.size(); i++){
            row[0] = list.get(i).getMaNV();
            row[1] = list.get(i).getTenNV();
            row[2] = list.get(i).getDoanhThu();
            model.addRow(row);
        }
    }
    
